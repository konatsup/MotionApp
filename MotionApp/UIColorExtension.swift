import UIKit

extension UIColor {
    class func blackColor() -> UIColor{
        let color = UIColor(red: 22/255, green: 25/255, blue: 41/255, alpha: 1.0)
        return color
    }
    
    class func whiteColor() -> UIColor{
        let color = UIColor(red: 233/255, green: 233/255, blue: 233/255, alpha: 1.0)
        return color
    }
    
    class func blueColor() -> UIColor{
        let color = UIColor(red: 27/255, green: 237/255, blue: 230/255, alpha: 1.0)
        return color
    }
}
